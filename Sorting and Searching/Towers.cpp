void solve(){
    int n; cin>>n;
    vector<int> nums(n);
    multiset<int> st;
    for(auto &x : nums){
        cin>>x;
        auto itr = st.upper_bound(x);
        if(itr == st.end()) st.insert(x);
        else{
            st.erase(itr);
            st.insert(x);
        }
    }
    cout<<st.size()<<endl;
}
