void solve(){
    string s; cin>>s;
    int ans = 1;
    int left = 0;
    int right = 0;
    while(right < s.size()){
        if(s[left] == s[right]){
            right++;
            ans = max(ans, right - left);
        }
        else{
            left = right;
        }
    }
    cout<<ans;
}
